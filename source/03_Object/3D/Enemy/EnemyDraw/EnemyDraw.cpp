//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//
// [ 初期化関数 ]
//
//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

void EnemyDraw::Init()
{
	// ステートの変更
	SetState(DrawComponent::State::FIXED_ALPHA);
}



//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//
// [ 終了処理関数 ]
//
//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

void EnemyDraw::Uninit()
{
	
}



//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//
// [ 描画関数 ]
//
//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

void EnemyDraw::Draw(unsigned mesh_index)
{
	mesh_index = mesh_index;
	
	// 敵にダウンキャスト
	Enemy* enemy = (Enemy*)GetGameObjectOrigin();

	// キューブ描画
	enemy->cube_polygon_->Draw();
}


//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//
// [ メッシュ数取得関数 ]
//
//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

unsigned EnemyDraw::GetMeshNum()
{
	// 敵にダウンキャスト
	Enemy* enemy = (Enemy*)GetGameObjectOrigin();

	// メッシュ数の取得
	return enemy->cube_polygon_->GetMeshNum();
}



//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//
// [ マテリアルの取得関数 ]
//
//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

D3DMATERIAL9* EnemyDraw::GetMaterial(unsigned mesh_index)
{
	mesh_index = mesh_index;

	// 敵にダウンキャスト
	Enemy* enemy = (Enemy*)GetGameObjectOrigin();

	// メッシュ数の取得
	return enemy->cube_polygon_->GetMaterial();
}