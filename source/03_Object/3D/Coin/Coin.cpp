//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//
// [ 初期化関数 ]
//
//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

void Coin::Init(DrawComponent*      draw_component,
				CollisionComponent* collision_component)
{
	// プレーンポリゴンの生成
	polygon_ = new PlanePolygon();

	// 基底クラスの初期化
	Init_SuperClass(nullptr, draw_component, collision_component);
}



//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//
// [ 終了処理関数 ]
//
//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

void Coin::Uninit()
{
	// 基底クラスの終了処理
	Uninit_SuperClass();

	// 各種消去
	SafeRelease::Normal(&polygon_);
}



