#include "SceneBace.h"



//****************************************
// 非静的メンバ関数定義
//****************************************
//--------------------------------------------------
// +初期化関数
//--------------------------------------------------
void SceneBace::Init()
{
	if (state_ == nullptr) return;
	state_->Init();
}



//--------------------------------------------------
// +終了関数
//--------------------------------------------------
void SceneBace::Uninit()
{
	SafeRelease::PlusUninit(&state_);
}



//--------------------------------------------------
// +更新関数
//--------------------------------------------------
void SceneBace::Update()
{
	if (state_ == nullptr) return;
	state_->Update();
}



//--------------------------------------------------
// +リセット関数
//--------------------------------------------------
void SceneBace::Reset()
{
	if (state_ == nullptr) return;
	state_->Reset();
}