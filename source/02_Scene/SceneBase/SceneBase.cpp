#include "SceneBase.h"



//****************************************
// 関数定義
//****************************************
//--------------------------------------------------
// +初期化関数
//--------------------------------------------------
void SceneBase::Init()
{
	if (state_ == nullptr) return;
	state_->Init();
}



//--------------------------------------------------
// +終了関数
//--------------------------------------------------
void SceneBase::Uninit()
{
	SafeRelease::PlusUninit(&state_);
}



//--------------------------------------------------
// +更新関数
//--------------------------------------------------
void SceneBase::Update()
{
	if (state_ == nullptr) return;
	state_->Update();
}



//--------------------------------------------------
// +リセット関数
//--------------------------------------------------
void SceneBase::Reset()
{
	if (state_ == nullptr) return;
	state_->Reset();
}



//--------------------------------------------------
// +ステート解放関数
//--------------------------------------------------
void SceneBase::ReleaseState()
{
	SafeRelease::Normal(&state_);
}